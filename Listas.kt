fun main() {

    // Lista apenas de leitura
    val formasLeitura = listOf("Triangulo", "Retangulo", "Quadrado")
    println(formasLeitura)
    
    // Lista que pode mudar 
    val formasMutaveis : MutableList <String> = mutableListOf("Triangulo", "Retangulo", "Quadrado")
    println(formasMutaveis)
    
}
